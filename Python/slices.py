nombre="Francisco"
print(nombre[0:3]) # Desde que indice arranco y cuantos caracteres quiero
print(nombre[3:])  # Arranca desde el índice 3 y toma todos los caracteres hacia adelante
print(nombre[1:7]) # Desde que indice uno al índice antes del 7
print(nombre[1:7:2]) # Desde que indice uno al índice antes del 7 en pasos de 2
print(nombre[1::3]) # Desde el indice uno hasta el final en pasos de 3
print(nombre[::-1]) # Desde el primer indice hasta el final en pasos negativos (devuelve el nombre al reves)



